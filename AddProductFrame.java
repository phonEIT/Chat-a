    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser jFileChooser = new JFileChooser();
        jFileChooser.setMultiSelectionEnabled(false);
        if (jFileChooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            // Lựa chọn của người dùng đã được xác nhận
            this.file = jFileChooser.getSelectedFile(); // Lấy tệp được chọn
            jLabel5.setText(file.getName()); // Hiển thị tên của tệp được chọn trong jLabel5
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            if (file == null) {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn một tập tin hình ảnh!!!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Product product = new Product();
            product.setName(jTextField2.getText().trim());
            product.setPrice(Double.parseDouble(jTextField1.getText().trim()));
            product.setImage(Files.readAllBytes(this.file.toPath()));

            if (dao.insertProduct(product)) {
                JOptionPane.showMessageDialog(null, "Đã thêm sản phẩm thành công..!");
                clear(); // Xóa các trường nhập và tệp được chọn sau khi thêm sản phẩm thành công
            } else {
                JOptionPane.showMessageDialog(null, "Thất bại rồi!!!", "Warning", 2);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "" + e, "Warning", 2);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

